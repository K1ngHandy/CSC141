## while loops
m = 1 # initial condition
while(m < 5):
    print(m) # body of loop
    m = m + 1 # update condition

## total of 1, 2, 3, 4, 5
n = 1
total = 0
while (n < 6):
    total = total + n
    n = n + 1
    print(total)
print("Total:", total) # prints final total when at start of line
